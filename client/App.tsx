declare global {
  interface Window {
    __REACT_APP_ROOT?: ReturnType<typeof createRoot>;
  }
}

const rootContainer = document.getElementById("root")!;
if (!(window as any).__REACT_APP_ROOT) {
  (window as any).__REACT_APP_ROOT = createRoot(rootContainer);
}

(window as any).__REACT_APP_ROOT.render(<App />);
